use leancrypto::lcr_sym::lcr_sym;
use leancrypto::lcr_sym::lcr_sym_type;

fn lc_rust_sym_one(sym_type: lcr_sym_type, key: &[u8], iv: &[u8], pt: &[u8],
		    ct: &mut [u8], exp_ct: &[u8]) {
	let mut sym = lcr_sym::new(sym_type);

	// One-shot encrypt
	let result = sym.setkey(key);
	assert_eq!(result, Ok(()));
	let result = sym.setiv(iv);
	assert_eq!(result, Ok(()));

	let result = sym.setiv(iv);
	assert_eq!(result, Ok(()));
	let result = sym.encrypt(pt, ct);
	assert_eq!(result, Ok(()));
	assert_eq!(ct, exp_ct);

	// One-shot decrypt
	let result = sym.setkey(key);
	assert_eq!(result, Ok(()));
	let result = sym.setiv(iv);
	assert_eq!(result, Ok(()));
	let mut new_pt = vec!(0u8; pt.len());
	let result = sym.decrypt(ct, &mut new_pt);
	assert_eq!(result, Ok(()));
	assert_eq!(pt, new_pt);
}

fn lc_rust_kw_one(sym_type: lcr_sym_type, key: &[u8], exp_iv: &[u8], pt: &[u8],
		  ct: &mut [u8], exp_ct: &[u8]) {
	let mut sym = lcr_sym::new(sym_type);

	// One-shot encrypt
	let result = sym.setkey(key);
	assert_eq!(result, Ok(()));
	let result = sym.kw_encrypt(pt, ct);
	assert_eq!(result, Ok(()));
	assert_eq!(&ct[0..8], exp_iv);
	assert_eq!(&ct[8..], exp_ct);

	// One-shot decrypt
	let result = sym.setkey(key);
	assert_eq!(result, Ok(()));
	let mut new_pt = vec!(0u8; pt.len());
	let result = sym.kw_decrypt(ct, &mut new_pt);
	assert_eq!(result, Ok(()));
	assert_eq!(pt, new_pt);
}

